'''
Here is a list of escape characters in Python:

\\n: newline
\\t: tab
\\r: carriage return
\\': single quote
\\": double quote
\\\: backslash
\\a: bell
\\b: backspace
\\f: form feed
\\n: newline
\\v: vertical tab
\\ooo: octal value
\\xhh: hexadecimal value
'''


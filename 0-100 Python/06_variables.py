# Type of variables

a = 10
b = 10.0
c = "10.0"
d = True
e = None
f = [1, 2, 3]
g = (1, 2, 3)
h = {1, 2, 3}
i = {"a": 1, "b": 2, "c": 3}
j = 1+2j
k = range(10)

print(type(a), a)
print(type(b), b)
print(type(c), c)
print(type(d), d)
print(type(e), e)
print(type(f), f)
print(type(g), g)
print(type(h), h)
print(type(i), i)
print(type(j), j)
print(type(k), k)
